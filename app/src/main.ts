import { NestFactory } from '@nestjs/core';
import { AppModule } from './app.module';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { ValidationPipe } from '@nestjs/common';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);
  const config = new DocumentBuilder()
    .setTitle('Portal do Aluno')
    .setDescription(
      'API para controlar o fluxo de autenticação, matrículas e registros do portal do aluno.',
    )
    .setVersion('BETA')
    .addBearerAuth(
      {
        type: 'http',
        scheme: 'bearer',
        bearerFormat: 'JWT',
        in: 'header',
        description: 'Insira o token fornecido ao logar: ',
      },
      'Token JWT',
    )
    .addSecurityRequirements('Token JWT')
    .build();
  const documentFactory = () => SwaggerModule.createDocument(app, config);
  SwaggerModule.setup(
    process.env.SWAGGER_PATH || 'swagger',
    app,
    documentFactory,
    {
      customSiteTitle: 'Portal do Aluno Docs',
    },
  );
  app.useGlobalPipes(new ValidationPipe());
  await app.listen(process.env.PORT ?? 3000);
}
void bootstrap();
